    /**
     * <p>getMenuBarBorder.</p>
     *
     * @return a {@link javax.swing.border.Border} object.
     */
    Border getMenuBarBorder();

    /**
     * <p>getMenuItemBorder.</p>
     *
     * @return a {@link javax.swing.border.Border} object.
     */
